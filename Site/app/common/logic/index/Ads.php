namespace app\common\logic\index;

use app\common\logic\BaseLogic;
use app\common\model\Ads as AdsModel;

class Ads extends BaseLogic
{
    /**
     * 获取列表
     * @param array $param
     * @return array
     */
    public function readList(array $param = []) : array
    {
        try {
            $list = AdsModel::where('type',$param['type'])
                ->where('is_show', 1)
                ->order('sequence,id', 'desc')
                ->paginate($param['size'])
                ->toArray();
            return [
                'code' => 0,
                'msg' => '',
                'count' => $list['total'],
                'data' => $list['data']
            ];
        } catch (\Exception $e) {
            return fail($e->getMessage());
        }
    }
}